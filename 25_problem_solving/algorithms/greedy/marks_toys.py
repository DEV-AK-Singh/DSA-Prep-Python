def maximumToys(prices, k):
    # Write your code here
    sort_prices = sorted(prices)
    current_cost = 0
    total_item = 0
    for price in sort_prices:
        cost = current_cost + price
        if cost <= k:
            total_item += 1
            current_cost = cost
        else:
            break
    return total_item        

