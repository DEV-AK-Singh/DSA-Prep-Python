def sockMerchant(n, ar):
    pairs = {}
    pairs_count = 0
    for socks in ar:
        if socks in pairs:
            pairs[socks] += 1
        else:
            pairs[socks] = 1
    for v in pairs.values():
        pairs_count += (v//2)
    return pairs_count

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    n = int(input().strip())

    ar = list(map(int, input().rstrip().split()))

    result = sockMerchant(n, ar)

    fptr.write(str(result) + '\n')

    fptr.close()
